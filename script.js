// Example for ++i (Pre-increment)
function preIncrementExample() {
    let i = 1;
    ++i;
    let x = 10 * i;
    console.log("Pre-increment example result (x):", x); // Output: 20
}

// Example for i++ (Post-increment)
function postIncrementExample() {
    let i = 1;
    let z = 10 * i;
    i++;
    console.log("Post-increment example result (z):", z); // Output: 10
}